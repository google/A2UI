import * as fs from 'fs';
import * as path from 'path';
import { URL } from 'url';

const OUTPUT_DIR = path.resolve('generated');
const OUTPUT_FILE = path.join(OUTPUT_DIR, 'catalogs-registry.ts');
const WHITELIST_FILE = path.resolve('config/whitelist.json');
const CATALOGS_FILE = path.resolve('config/catalogs.json');

console.log('⚡ A2UI Unified Catalog Compiler running...');

// 1. Load domain whitelist
if (!fs.existsSync(WHITELIST_FILE)) {
  console.error(`❌ Security Error: Whitelist configuration file not found at ${WHITELIST_FILE}`);
  process.exit(1);
}

let allowedDomains = [];
try {
  const whitelistData = JSON.parse(fs.readFileSync(WHITELIST_FILE, 'utf-8'));
  allowedDomains = whitelistData.allowedDomains || [];
} catch (err) {
  console.error('❌ Security Error: Failed to parse whitelist.json:', err.message);
  process.exit(1);
}

// 2. Handle --add CLI Argument to dynamically register new catalogs
const args = process.argv.slice(2);
if (args[0] === '--add' && args[1]) {
  const newUrl = args[1];
  console.log(`📥 Registering new catalog URL: "${newUrl}"...`);
  try {
    const parsed = new URL(newUrl);
    if (!allowedDomains.includes(parsed.hostname)) {
      console.error(`🚨 SECURITY REJECTION: Hostname "${parsed.hostname}" is not whitelisted in whitelist.json!`);
      process.exit(1);
    }
    
    let currentCatalogs = [];
    if (fs.existsSync(CATALOGS_FILE)) {
      currentCatalogs = JSON.parse(fs.readFileSync(CATALOGS_FILE, 'utf-8'));
    }
    if (!currentCatalogs.includes(newUrl)) {
      currentCatalogs.push(newUrl);
      fs.writeFileSync(CATALOGS_FILE, JSON.stringify(currentCatalogs, null, 2), 'utf-8');
      console.log(`✓ Catalog URL successfully added to config/catalogs.json!`);
    } else {
      console.log(`ℹ Catalog URL already exists in configuration.`);
    }
  } catch (err) {
    console.error('❌ Failed to add catalog URL:', err.message);
    process.exit(1);
  }
}

// 3. Load target catalogs
let targetCatalogs = [];
if (fs.existsSync(CATALOGS_FILE)) {
  try {
    targetCatalogs = JSON.parse(fs.readFileSync(CATALOGS_FILE, 'utf-8'));
  } catch (err) {
    console.error('❌ Failed to parse catalogs.json:', err.message);
    process.exit(1);
  }
}

if (targetCatalogs.length === 0) {
  targetCatalogs = [
    'https://a2ui.org/catalogs/v1/basic-catalog.json',
    'https://a2ui.org/catalogs/v1/weather-catalog.json',
    'https://a2ui.org/catalogs/v1/mcp-catalog.json'
  ];
}

// 4. Perform domain security checks
console.log('🔒 Performing domain security verification...');
for (const catalogUrl of targetCatalogs) {
  try {
    const parsedUrl = new URL(catalogUrl);
    const hostname = parsedUrl.hostname;
    
    if (!allowedDomains.includes(hostname)) {
      console.error(`🚨 SECURITY COMPILATION CRASH: Untrusted dynamic domain detected: "${hostname}"`);
      console.error(`   The URL "${catalogUrl}" is NOT permitted by config/whitelist.json!`);
      process.exit(1);
    }
    console.log(`   ✓ Domain verified: "${hostname}"`);
  } catch (err) {
    console.error(`❌ Error parsing catalog URL "${catalogUrl}":`, err.message);
    process.exit(1);
  }
}

// Create output folder if not exists
if (!fs.existsSync(OUTPUT_DIR)) {
  fs.mkdirSync(OUTPUT_DIR, { recursive: true });
}

// Dynamically generate the exports registry based on the loaded catalogs!
const weatherCatalogRegistered = targetCatalogs.some(url => url.includes('weather-catalog.json'));
const mcpCatalogRegistered = targetCatalogs.some(url => url.includes('mcp-catalog.json'));

// Check if a mock 4th FloorPlan catalog has been added by the user
const floorplanCatalogRegistered = targetCatalogs.some(url => url.includes('floorplan-catalog.json'));

const registryContent = `/*
 * Generated by A2UI Pre-Build Catalog Compiler.
 * DO NOT EDIT THIS FILE DIRECTLY.
 */

import { Catalog } from '@a2ui/web_core/v0_9';
import { basicCatalog } from '@a2ui/lit/v0_9';

// Static Imports of custom components resolved from the catalogs
import { WeatherWidget } from '../components/weather-widget.js';
import { McpApp } from '../components/mcp-app.js';

const basicComponents = Array.from(basicCatalog.components.values());

export const clientBasicCatalog = new Catalog(
  'https://a2ui.org/catalogs/v1/basic-catalog.json',
  basicComponents
);

${weatherCatalogRegistered ? `
export const clientWeatherCatalog = new Catalog(
  'https://a2ui.org/catalogs/v1/weather-catalog.json',
  [
    ...basicComponents,
    WeatherWidget as any
  ]
);
` : ''}

${mcpCatalogRegistered ? `
export const clientMcpCatalog = new Catalog(
  'https://a2ui.org/catalogs/v1/mcp-catalog.json',
  [
    ...basicComponents,
    McpApp as any
  ]
);
` : ''}

${floorplanCatalogRegistered ? `
// Statically compiled 4th catalog added dynamically by administrator!
export const clientFloorplanCatalog = new Catalog(
  'https://a2ui.org/catalogs/v1/floorplan-catalog.json',
  [
    ...basicComponents
  ]
);
` : ''}

export const catalogsRegistry: Record<string, Catalog<any>> = {
  basic: clientBasicCatalog,
  ${weatherCatalogRegistered ? 'weather: clientWeatherCatalog,' : ''}
  ${mcpCatalogRegistered ? 'mcp: clientMcpCatalog,' : ''}
  ${floorplanCatalogRegistered ? 'floorplan: clientFloorplanCatalog,' : ''}
};

export type CatalogKey = 'basic' ${weatherCatalogRegistered ? "| 'weather'" : ''} ${mcpCatalogRegistered ? "| 'mcp'" : ''} ${floorplanCatalogRegistered ? "| 'floorplan'" : ''};
`;

fs.writeFileSync(OUTPUT_FILE, registryContent, 'utf-8');
console.log('✓ Static catalog registries generated successfully!');
